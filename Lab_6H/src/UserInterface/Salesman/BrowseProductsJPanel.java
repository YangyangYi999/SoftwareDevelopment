    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTableProduct.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a row!");
            return;
        }
        Product selectedProduct = (Product)jTableProduct.getValueAt(selectedRow, 0);
        int fetchQuantity = (Integer)jSpinnerQuantity.getValue();
        if(fetchQuantity <=0){
            JOptionPane.showMessageDialog(null, "Quantiy cannot be less than 0!");
            return;
        }
        try{
            double salesPrice = Double.parseDouble(txtSalesPrice.getText());
            if(salesPrice <= selectedProduct.getPrice()){
                JOptionPane.showMessageDialog(null, "SalesPrice cannot be less than actual price!");
                return;
            }
            if(fetchQuantity <= selectedProduct.getAvail()){
                boolean alreadyPresent = false;
                for(OrderItem oi: order.getOrderItemList()){
                    if(oi.getProduct() == selectedProduct){
                        int oldAvail = selectedProduct.getAvail();
                        int newAvail = oldAvail - fetchQuantity;
                        selectedProduct.setAvail(newAvail);
                        oi.setQuantity(fetchQuantity+oi.getQuantity());
                        alreadyPresent = true;
                        populateTable();
                        refreshOrderTable();
                        break;
                    }
                }
                if(!alreadyPresent){
                    int oldAvail = selectedProduct.getAvail();
                    int newavail = oldAvail - fetchQuantity;
                    selectedProduct.setAvail(newavail);
                    order.addOrderItem(selectedProduct, fetchQuantity, salesPrice);
                    populateTable();
                    refreshOrderTable();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Quantiy > Availability!","Waring",JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid SalesPrice!");
            return;
        }
        
    }//GEN-LAST:event_btnAddToCartActionPerformed

