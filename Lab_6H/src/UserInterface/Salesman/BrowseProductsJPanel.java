    public void populateSupplierCombo(){
        jComboBoxSupplier1.removeAllItems();
        for(Supplier s: supplierDirectory.getSupplierDirectory()){
            jComboBoxSupplier1.addItem(s);
        }
        populateTable();
    }
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)jTableProduct.getModel();
        dtm.setRowCount(0);
        if(jComboBoxSupplier1.getSelectedItem()==null){
            return;
        }
        Supplier supplier = (Supplier)jComboBoxSupplier1.getSelectedItem();
        for(Product p :supplier.getProductCatalog().getProductCatalog()){
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getModelNum();
            
            row[2] = p.getAvail();
            dtm.addRow(row);
        }
    }
    public void refreshProductTable(String keyword){
        DefaultTableModel dtm = (DefaultTableModel)jTableProduct.getModel();
        dtm.setRowCount(0);   
        for(Supplier s: supplierDirectory.getSupplierDirectory()){
            for(Product p: s.getProductCatalog().getProductCatalog()){
                if(p.getProductName().equalsIgnoreCase(keyword)){
                    Object row[] = new Object[4];
                    row[0] = p;
                    row[1] = p.getModelNum();
                    
                    row[2] = p.getAvail();
                    dtm.addRow(row);
                }
            }
        }
    }
    
    public void refreshOrderTable(){
        DefaultTableModel dtm = (DefaultTableModel)jTableOrder.getModel();
        dtm.setRowCount(0);   
        for(OrderItem oi: order.getOrderItemList()){
            Object row[] = new Object[2];
            row[0] = oi;
            
            row[1] = oi.getQuantity();
            
            dtm.addRow(row);
        }
    }
