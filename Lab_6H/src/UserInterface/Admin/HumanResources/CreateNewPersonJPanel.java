    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if(txtFirstName.getText().equals("")&&txtLastName.getText().equals("")){
            JOptionPane.showMessageDialog(null,"The first name and last name cannot all be empty!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        
        Person person = business.getPersonDirectory().addPerson();
        person.setFirstName(firstName);
        person.setLastName(lastName);
        
        JOptionPane.showMessageDialog(null,"The person successfully created!");

        populate();
    }//GEN-LAST:event_btnCreateActionPerformed


