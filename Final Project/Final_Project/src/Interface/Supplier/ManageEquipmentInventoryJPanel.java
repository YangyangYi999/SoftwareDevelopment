    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) equipmentMngJTable.getModel();
        
        model.setRowCount(0);
        
        for (Equipment e :organization.getEquipmentDirectory().getEquipmentList()){
            Object[] row = new Object[2];
            row[0] = e;
            row[1] = e.getQuantity();
            
            model.addRow(row);
        }
    }
