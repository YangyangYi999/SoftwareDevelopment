    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(networkComboBox.getSelectedItem()==null||stateName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Please check the combobox and the textfield");
        }else{
            Network network = (Network)networkComboBox.getSelectedItem();
            network.getStateDirectory().getStateList().add(new State(stateName.getText()));
            stateName.setText("");
            populateTable(network);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int select = stateTable.getSelectedRow();
    if(select<0||networkComboBox.getSelectedItem()==null){
        JOptionPane.showMessageDialog(this, "Invalid");
    }else{
        Network network = (Network)networkComboBox.getSelectedItem();
        network.getStateDirectory().getStateList().remove( ((State)stateTable.getValueAt(select,0)));
         populateTable(network);
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int select = stateTable.getSelectedRow();
    if(select<0||networkComboBox.getSelectedItem()==null){
        JOptionPane.showMessageDialog(this, "Invalid");
    }else{
        ((State)stateTable.getValueAt(select,0)).setName(newName.getText());
        Network network = (Network)networkComboBox.getSelectedItem();
         populateTable(network);
         newName.setText("");
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed


