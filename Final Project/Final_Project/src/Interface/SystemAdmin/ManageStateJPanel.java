    void populateTable(Network network){
        DefaultTableModel dtm = (DefaultTableModel)stateTable.getModel();
        dtm.setRowCount(0);
        for(State state: network.getStateDirectory().getStateList()){
            Object[] row = new Object[1];
            row[0]=state;
            dtm.addRow(row);
        }
    }
    void populateComboBox(){
        networkComboBox.removeAllItems();
        for(Network n : system.getNetworkList().getNetworkList()){
            networkComboBox.addItem(n);
        }
    }
