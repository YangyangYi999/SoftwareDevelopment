    void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)networkTable.getModel();
        dtm.setRowCount(0);
        for(Network network: system.getNetworkList().getNetworkList()){
            Object[] row = new Object[1];
            row[0]=network;
            dtm.addRow(row);
        }
    }

